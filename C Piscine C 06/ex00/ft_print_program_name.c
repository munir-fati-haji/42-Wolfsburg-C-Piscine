#include <unistd.h>

int	main(int arg, char **argc)
{
	int	i;

	i = 0;
	if (arg != 0)
	{
		while (argc[0][i] != 0)
		{
			write(1, &argc[0][i], 1);
			i++;
		}
		write(1, '\n', 1);
	}
}
