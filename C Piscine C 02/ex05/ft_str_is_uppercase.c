int	ft_str_is_uppercase(char *str)
{
	int	r;
	int	i;

	i = 0;
	if (str[i] == '\0')
	{
		r = 1;
	}
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			r = 0;
			break ;
		}
		else
		{
			r = 1;
		}
		i++;
	}
	return (r);
}
