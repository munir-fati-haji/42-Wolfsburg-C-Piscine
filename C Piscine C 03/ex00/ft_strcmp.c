int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	d;

	i = 0;
	d = 0;
	while ((s1[i] != 0) || (s2[i] != 0))
	{
		d = s1[i] - s2[i];
		i++;
		if (d != 0)
			break ;
	}
	return (d);
}
